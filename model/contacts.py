class Contact:
    def __init__(self, firstName, middleName, lastName,
                 nickName, titleName, companyName,
                 addressName, homeNumber, mobileNumber,
                 workNumber, faxNumber, email,
                 personalWebsite, birthDay, birthMonth, birthYear, annivarsaryDay, annivarsaryMonth,
                 annivarsaryYear):
        self.firstName = firstName
        self.middleName = middleName
        self.lastName = lastName
        self.nickName = nickName
        self.titleName = titleName
        self.companyName = companyName
        self.addressName = addressName
        self.homeNumber = homeNumber
        self.mobileNumber = mobileNumber
        self.workNumber = workNumber
        self.faxNumber = faxNumber
        self.email = email
        self.personalWebsite = personalWebsite
        self.birthDay = birthDay
        self.birthMonth = birthMonth
        self.birthYear = birthYear
        self.annivarsaryDay = annivarsaryDay
        self.annivarsaryMonth = annivarsaryMonth
        self.annivarsaryYear = annivarsaryYear
